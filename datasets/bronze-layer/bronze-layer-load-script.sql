/* 
===============================================================================================
Creating Stored Procedures for easier repeatability of the SQL Code 
===============================================================================================

All data will be full loaded into the bronze layer using drop and full load method.  

*/ 

CREATE or ALTER PROCEDURE bronze.load_raw_data_into_bronze_layer AS 
BEGIN
/* Creating batch and per table start and end time for monitoring the query efficiency on each table level and stored procedure level */
	DECLARE @start_time DATETIME, @end_time DATETIME, @batch_start_time DATETIME, @batch_end_time DATETIME; 
	BEGIN TRY
		SET @batch_start_time = GETDATE();
